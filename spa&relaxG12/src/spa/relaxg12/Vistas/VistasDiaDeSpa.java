     public  void cargarTabla(){
        DefaultTableModel modelo =(DefaultTableModel) tblSesiones.getModel();
         
         modelo.setRowCount(0);
         for (Sesion aux : listado){
             Object [] fila= {
                 aux.getTratamientos(),
                 aux.getProfesional(),
                 aux.getInstalacion(),
                 
               
             };
             modelo.addRow(fila);
         }
     }
